let num1 = 10;
let num2 = 5;
// Addition
let add = num1 + num2;
console.log(num1 + " + " + num2 + " = " + add);
console.log(`${num1} + ${num2} = ${add}`);
// Subtraction
console.log(num1 + " - " + num2 + " = " + (num1 - num2));
// Multiplication
console.log(num1 + " * " + num2 + " = " + (num1 * num2));
// Division
console.log(num1 + " / " + num2 + " = " + (num1 / num2));
// Modulus
console.log(num1 + " % " + num2 + " = " + (num1 % num2));