let a = 10;
let b = 20;
let c = "10";

// comparison operators
console.log("a > b: " + (a > b)); // false
console.log("a < b: " + (a < b)); // true
console.log("a == c: " + (a == c)); // true (loose equality) check value only
console.log("a === c: " + (a === c)); // false (strict equality) check value and type 
console.log("a != b: " + (a != 10)); // true (loose inequality) check value only