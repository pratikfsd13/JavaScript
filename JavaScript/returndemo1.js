function add(num1, num2) {
    return num1 + num2;
}
function mul(num1, num2) {
    return num1 * num2;
} function div(num1, num2) {
    return num1 / num2;
} function sub(num1, num2) {
    return num1 - num2;
}
let result1 = add(50, 10);
let result2 = mul(50, 10);
console.log("Addition = " + result1);
console.log("Multiplication = " + result2); // store value in variable and print

console.log("Division = " + div(50, 10));
console.log("Subtraction = " + sub(50, 10)); // print value directly without storing in variable
