let x=10;
console.log("x: " + x); // 10
x += 5; // equivalent to x = x + 5
console.log("x: " + x);// 15

x-= 3; // equivalent to x = x - 3
console.log("x: " + x); // 12

x *= 2; // equivalent to x = x * 2
console.log("x: " + x); // 24       

x /= 4; // equivalent to x = x / 4
console.log("x: " + x); // 6

x %= 4; // equivalent to x = x % 4
console.log("x: " + x); // 2 (remainder of 6 divided by 4)  
